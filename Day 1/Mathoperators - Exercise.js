num = 1 //num equals 1
num = num + 5 //num equals 6

// assignment
num += 5 // num equals 11
num -= 1 // num equals 10
num *= 3 // num equals 30
num /= 0.5 // num equals 60

// increment and decrement
num++ //num equals 61
num-- //num equals 60

// operators before the variable
// what's the difference?
++num //num equals 61
--num //num equals 60
